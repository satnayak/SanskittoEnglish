# Unicode-aware tokenization.
# Sanskrit: whitespace + punctuation separation; English: words/numbers/punctuation.
PUNCT_RE = re.compile(r'([।॥!?.,;:"()\[\]{}])')

def normalize_text(s):
    s = unicodedata.normalize("NFC", str(s))
    s = re.sub(r"\s+", " ", s).strip()
    return s

def tokenize_sa(s):
    s = normalize_text(s)
    s = PUNCT_RE.sub(r" \1 ", s)
    return s.split()

def tokenize_en(s):
    s = normalize_text(s).lower()
    return re.findall(r"[a-z]+(?:'[a-z]+)?|\d+(?:\.\d+)?|[^\w\s]", s, flags=re.UNICODE)

