dev_loader=DataLoader(ParallelDataset(dev_df),batch_size=64,shuffle=False,collate_fn=collate)

class Encoder(nn.Module):
    def __init__(self, vocab, emb=256, hid=256, dropout=.3):
        super().__init__()
        self.emb=nn.Embedding(vocab,emb,padding_idx=SRC.pad)
        self.rnn=nn.GRU(emb,hid,batch_first=True,bidirectional=True)
        self.fc=nn.Linear(hid*2,hid)
        self.drop=nn.Dropout(dropout)
    def forward(self,x):
        e=self.drop(self.emb(x))
        out,h=self.rnn(e)
        h=torch.tanh(self.fc(torch.cat([h[-2],h[-1]],dim=1)))
        return out,h

class BahdanauAttention(nn.Module):
    def __init__(self, enc_hid=256, dec_hid=256):
        super().__init__()
        self.W=nn.Linear(enc_hid*2,dec_hid)
        self.U=nn.Linear(dec_hid,dec_hid)
        self.v=nn.Linear(dec_hid,1,bias=False)
    def forward(self,h,enc_out,mask):
        score=self.v(torch.tanh(self.W(enc_out)+self.U(h).unsqueeze(1))).squeeze(-1)
        score=score.masked_fill(~mask,-1e9)
        return F.softmax(score,dim=1)

class Decoder(nn.Module):
    def __init__(self,vocab,emb=256,enc_hid=256,dec_hid=256,dropout=.3):
        super().__init__()
        self.emb=nn.Embedding(vocab,emb,padding_idx=TRG.pad)
        self.attn=BahdanauAttention(enc_hid,dec_hid)
        self.rnn=nn.GRU(emb+enc_hid*2,dec_hid,batch_first=True)
        self.fc=nn.Linear(dec_hid+enc_hid*2+emb,vocab)
        self.drop=nn.Dropout(dropout)
    def forward(self,y,h,enc_out,mask):
        e=self.drop(self.emb(y)).unsqueeze(1)
        a=self.attn(h,enc_out,mask)
        ctx=torch.bmm(a.unsqueeze(1),enc_out)
        o,h2=self.rnn(torch.cat([e,ctx],dim=2),h.unsqueeze(0))
        logits=self.fc(torch.cat([o.squeeze(1),ctx.squeeze(1),e.squeeze(1)],dim=1))
        return logits,h2.squeeze(0),a

class Seq2Seq(nn.Module):
    def __init__(self,enc,dec):
        super().__init__(); self.enc=enc; self.dec=dec
    def forward(self,src,trg,teacher_forcing=.5):
        enc_out,h=self.enc(src); mask=src.ne(SRC.pad)
        y=trg[:,0]; outs=[]
        for t in range(1,trg.size(1)):
            logits,h,_=self.dec(y,h,enc_out,mask)
            outs.append(logits.unsqueeze(1))
            y = trg[:,t] if random.random()<teacher_forcing else logits.argmax(1)
        return torch.cat(outs,1)

model=Seq2Seq(Encoder(len(SRC)),Decoder(len(TRG))).to(DEVICE)
print("Parameters:", sum(p.numel() for p in model.parameters() if p.requires_grad))

criterion=nn.CrossEntropyLoss(ignore_index=TRG.pad)
optimizer=torch.optim.AdamW(model.parameters(),lr=3e-4,weight_decay=1e-5)

def run_epoch(loader,train=True,tf=.5):
    model.train(train); total=0
    for src,trg in loader:
        src,trg=src.to(DEVICE),trg.to(DEVICE)
        if train: optimizer.zero_grad()
        with torch.set_grad_enabled(train):
            out=model(src,trg,teacher_forcing=tf if train else 0.0)
            loss=criterion(out.reshape(-1,out.size(-1)),trg[:,1:].reshape(-1))
            if train:
                loss.backward()
                nn.utils.clip_grad_norm_(model.parameters(),1.0)
                optimizer.step()
        total += loss.item()*src.size(0)
    return total/len(loader.dataset)

