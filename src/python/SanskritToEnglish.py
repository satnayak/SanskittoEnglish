MAX_SRC_LEN = 80
MAX_TRG_LEN = 80

class ParallelDataset(Dataset):
    def __init__(self, df):
        self.df=df.reset_index(drop=True)
    def __len__(self): return len(self.df)
    def __getitem__(self,i):
        r=self.df.iloc[i]
        s=SRC.encode(tokenize_sa(r.Sentence_sa))[:MAX_SRC_LEN]
        t=TRG.encode(tokenize_en(r.Sentence_en))[:MAX_TRG_LEN]
        if s[-1] != SRC.eos: s[-1]=SRC.eos
        if t[-1] != TRG.eos: t[-1]=TRG.eos
        return torch.tensor(s), torch.tensor(t)

def collate(batch):
    ss,tt=zip(*batch)
    ss=nn.utils.rnn.pad_sequence(ss,batch_first=True,padding_value=SRC.pad)
    tt=nn.utils.rnn.pad_sequence(tt,batch_first=True,padding_value=TRG.pad)
    return ss,tt

