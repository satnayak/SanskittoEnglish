@torch.no_grad()
def translate(sentence,max_len=80):
    model.eval()
    toks=tokenize_sa(sentence)
    ids=SRC.encode(toks)[:MAX_SRC_LEN]
    if ids[-1]!=SRC.eos: ids[-1]=SRC.eos
    src=torch.tensor(ids).unsqueeze(0).to(DEVICE)
    enc_out,h=model.enc(src); mask=src.ne(SRC.pad)
    y=torch.tensor([TRG.bos],device=DEVICE)
    pred=[]; attns=[]
    for _ in range(max_len):
        logits,h,a=model.dec(y,h,enc_out,mask)
        nxt=logits.argmax(1).item()
        if nxt==TRG.eos: break
        pred.append(nxt); attns.append(a.squeeze(0).cpu().numpy())
        y=torch.tensor([nxt],device=DEVICE)
    return " ".join(TRG.decode(pred)), toks, np.array(attns)

