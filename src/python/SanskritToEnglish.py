SPECIALS = ["<pad>", "<unk>", "<bos>", "<eos>"]

class Vocab:
    def __init__(self, token_lists, min_freq=2, max_size=None):
        c = Counter(t for xs in token_lists for t in xs)
        words = [w for w,f in c.most_common() if f >= min_freq]
        if max_size: words = words[:max_size-len(SPECIALS)]
        self.itos = SPECIALS + [w for w in words if w not in SPECIALS]
        self.stoi = {w:i for i,w in enumerate(self.itos)}
        self.pad, self.unk, self.bos, self.eos = [self.stoi[x] for x in SPECIALS]
    def encode(self, toks, add_bos_eos=True):
        ids = [self.stoi.get(t, self.unk) for t in toks]
        return ([self.bos]+ids+[self.eos]) if add_bos_eos else ids
    def decode(self, ids):
        out=[]
        for i in ids:
            w=self.itos[int(i)]
            if w=="<eos>": break
            if w not in SPECIALS: out.append(w)
        return out
    def __len__(self): return len(self.itos)

sa_train_tok = [tokenize_sa(x) for x in train_df.Sentence_sa]
